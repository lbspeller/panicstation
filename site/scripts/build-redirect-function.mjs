import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const SCRIPT_DIR = dirname(fileURLToPath(import.meta.url));
const PROJECT_ROOT = resolve(SCRIPT_DIR, "..");
const CSV_PATH = join(
  PROJECT_ROOT,
  "redirects",
  "cloudflare_bulk_redirects_confirmed.csv"
);
const OUTPUT_PATH = join(PROJECT_ROOT, "functions", "_middleware.js");
const SITE_ORIGIN = "https://panicstation.org";

function parseCsv(text) {
  const rows = [];
  let row = [];
  let field = "";
  let quoted = false;

  for (let index = 0; index < text.length; index += 1) {
    const character = text[index];

    if (quoted) {
      if (character === '"') {
        if (text[index + 1] === '"') {
          field += '"';
          index += 1;
        } else {
          quoted = false;
        }
      } else {
        field += character;
      }
      continue;
    }

    if (character === '"') {
      quoted = true;
      continue;
    }

    if (character === ",") {
      row.push(field);
      field = "";
      continue;
    }

    if (character === "\n") {
      row.push(field.replace(/\r$/, ""));
      field = "";

      if (row.some((value) => value.trim() !== "")) {
        rows.push(row);
      }

      row = [];
      continue;
    }

    field += character;
  }

  if (quoted) {
    throw new Error("The redirect CSV ends inside a quoted field.");
  }

  row.push(field.replace(/\r$/, ""));
  if (row.some((value) => value.trim() !== "")) {
    rows.push(row);
  }

  return rows;
}

function normalizePathname(pathname) {
  let value = pathname.replace(/\/{2,}/g, "/");

  if (value.length > 1 && !value.endsWith("/")) {
    value += "/";
  }

  return value;
}

function readRedirectRows(csvText) {
  const redirects = new Map();
  const rows = parseCsv(csvText.replace(/^\uFEFF/, ""));

  rows.forEach((row, rowIndex) => {
    const lineNumber = rowIndex + 1;
    const sourceValue = String(row[0] || "").trim();
    const targetValue = String(row[1] || "").trim();
    const statusValue = String(row[2] || "301").trim();

    if (
      lineNumber === 1 &&
      /source/i.test(sourceValue) &&
      /target/i.test(targetValue)
    ) {
      return;
    }

    if (!sourceValue || !targetValue) {
      throw new Error(`Redirect CSV line ${lineNumber} has a blank source or target.`);
    }

    if (statusValue !== "301") {
      throw new Error(
        `Redirect CSV line ${lineNumber} uses status ${statusValue}; this setup requires 301.`
      );
    }

    let sourceUrl;
    let targetUrl;

    try {
      sourceUrl = new URL(sourceValue);
      targetUrl = new URL(targetValue);
    } catch {
      throw new Error(`Redirect CSV line ${lineNumber} contains an invalid URL.`);
    }

    if (sourceUrl.origin !== SITE_ORIGIN || targetUrl.origin !== SITE_ORIGIN) {
      throw new Error(
        `Redirect CSV line ${lineNumber} must use ${SITE_ORIGIN} for both URLs.`
      );
    }

    if (sourceUrl.search || sourceUrl.hash) {
      throw new Error(
        `Redirect CSV line ${lineNumber} has a query string or fragment in its source URL.`
      );
    }

    const sourcePath = normalizePathname(sourceUrl.pathname);
    const targetPath = normalizePathname(targetUrl.pathname);

    if (!/^\/(uk|us)\//.test(sourcePath)) {
      throw new Error(
        `Redirect CSV line ${lineNumber} is outside the /uk/ or /us/ guide routes.`
      );
    }

    if (sourcePath === targetPath) {
      throw new Error(`Redirect CSV line ${lineNumber} redirects a URL to itself.`);
    }

    const existingTarget = redirects.get(sourcePath);
    if (existingTarget && existingTarget !== targetUrl.toString()) {
      throw new Error(
        `Redirect CSV line ${lineNumber} conflicts with another redirect for ${sourcePath}.`
      );
    }

    redirects.set(sourcePath, targetUrl.toString());
  });

  for (const [sourcePath, targetUrl] of redirects) {
    const targetPath = normalizePathname(new URL(targetUrl).pathname);
    if (redirects.has(targetPath)) {
      throw new Error(
        `Redirect chain detected: ${sourcePath} points to another redirect source (${targetPath}).`
      );
    }
  }

  if (redirects.size === 0) {
    throw new Error("No redirects were found in the CSV.");
  }

  return redirects;
}

function generateMiddleware(redirects) {
  const redirectObject = Object.fromEntries(
    [...redirects.entries()].sort(([left], [right]) => left.localeCompare(right))
  );

  return `// AUTO-GENERATED by scripts/build-redirect-function.mjs.\n` +
`// Edit redirects/cloudflare_bulk_redirects_confirmed.csv, not this file.\n\n` +
`const REDIRECTS = Object.freeze(${JSON.stringify(redirectObject, null, 2)});\n\n` +
`function normalizePathname(pathname) {\n` +
`  let value = pathname.replace(/\\/{2,}/g, "/");\n\n` +
`  if (value.length > 1 && !value.endsWith("/")) {\n` +
`    value += "/";\n` +
`  }\n\n` +
`  return value;\n` +
`}\n\n` +
`export async function onRequest(context) {\n` +
`  const method = context.request.method.toUpperCase();\n\n` +
`  // Guide pages are read-only. Do not redirect POST or other non-navigation requests.\n` +
`  if (method !== "GET" && method !== "HEAD") {\n` +
`    return context.next();\n` +
`  }\n\n` +
`  const requestUrl = new URL(context.request.url);\n` +
`  const sourcePath = normalizePathname(requestUrl.pathname);\n` +
`  const targetUrl = REDIRECTS[sourcePath];\n\n` +
`  if (!targetUrl) {\n` +
`    return context.next();\n` +
`  }\n\n` +
`  return Response.redirect(targetUrl, 301);\n` +
`}\n`;
}

const csvText = await readFile(CSV_PATH, "utf8");
const redirects = readRedirectRows(csvText);
const middleware = generateMiddleware(redirects);

await mkdir(dirname(OUTPUT_PATH), { recursive: true });
await writeFile(OUTPUT_PATH, middleware, "utf8");

console.log(
  `Generated functions/_middleware.js with ${redirects.size.toLocaleString("en-GB")} permanent redirects.`
);
